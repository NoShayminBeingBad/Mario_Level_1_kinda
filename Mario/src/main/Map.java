	private int[][] grid;
	
	public Map(Game game) {
		this.game = game;
		
		BufferedImage image = Game.toBufferedImage(new ImageIcon("res/TileSet.png").getImage());
		tile =  new BufferedImage[image.getWidth() / 16];
		for (int i = 0; i < image.getWidth() / 16; i++) {
			tile[i] = image.getSubimage(16 * i, 0, 16, 16);
		}
		//adds flag, goombas, and coins
		flag = new Flag(197, 1, this);
		entities.add(new Goomba(30, 5, this));
		entities.add(flag);
		entities.add(new Goomba(100, 5, this));
		entities.add(new Goomba(130, 5, this));
		entities.add(new Goomba(170, 5, this));
		entities.add(new Goomba(171, 5, this));
		entities.add(new Goomba(50, 5, this));
		entities.add(new Goomba(55, 5, this));
		entities.add(new Goomba(60, 5, this));
		grid = new int[resetGrid.length][resetGrid[0].length];
		for (int i = 0; i < resetGrid.length; i++) {
			grid[i] = resetGrid[i].clone();
		}
		for (int i = 50; i < 70; i++) {
			for (int j = 2; j < 6; j++) {
				if (grid[j][i] == 0) {
					entities.add(new Coin(i, j, this));
				}
			}
		}
	}
	
