	public int getCollision(int x, int y) {
		try {
			return grid[y][x];
		}catch (Exception e) {
			return 0;
		}
	}
	
	public int getCollision(int[] arr) {
		try {
			return grid[arr[1]][arr[0]];
		}catch (Exception e) {
			return 0;
		}
	}
	
	public Rectangle getRec(int x, int y) {
		if (getCollision(x, y) == 0 || getCollision(x, y) == 16 || getCollision(x, y) == 15) {
			return null;
		}else {
			return new Rectangle (x * 48, y * 48, 48, 48);
		}
	}
	
