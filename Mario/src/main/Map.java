	//Sets a punched block
	public void setPunchedBlock(int x, int y) {
		if (hitFrame == -1) {
			hit[0] = x;
			hit[1] = y;
			hitFrame = 0;
		}
	}
	
	public void reset() {
		for (int i = 0; i < grid.length; i++) {
			grid[i] = resetGrid[i].clone();
		}
		mapOffSetX = 0;
	}
	
	public void tick() {
		Mario mario = game.getMario();
		if (!mario.isDead()) {
			//if a block is punched, an event will run
			if (hit[0] != -1 && hit[1] != -1) {
				if (hitFrame < 10) {
					hitFrame++;
					if (mario.getHeight() > 1 && grid[hit[1]][hit[0]] == 3) {
						grid[hit[1]][hit[0]] = 4;
					}
					if (hitFrame > 3 && grid[hit[1]][hit[0]] == 4) {
						grid[hit[1]][hit[0]] = 0;
						hit[0] = -1;
						hit[1] = -1;
						hitFrame = -1;
					}
				}else if (hitFrame < 11){
					hitFrame = -1;
					if (grid[hit[1]][hit[0]] == 5) {
						entities.add(new Mushroom(hit[0], hit[1] - 1, this));
						grid[hit[1]][hit[0]] = 9;
					}else if (grid[hit[1]][hit[0]] == COINBLOCK) {
						grid[hit[1]][hit[0]] = 9;
						entities.add(new UpCoin(hit[0], hit[1], this));
						game.getMario().addScore(Game.COIN_SCORE);
						game.getMario().addCoin();
					}else if (grid[hit[1]][hit[0]] > COINBLOCK) {
						grid[hit[1]][hit[0]]--;
						if (grid[hit[1]][hit[0]] == COINBLOCK) {
							grid[hit[1]][hit[0]] = 9;
						}
						entities.add(new UpCoin(hit[0], hit[1], this));
						game.getMario().addScore(Game.COIN_SCORE);
						game.getMario().addCoin();
					}
					hit[0] = -1;
					hit[1] = -1;
				}
			}
			//only draws certain part of the map so 
			int minx = Math.max(game.getMario().gridX() - 20, 0);
			int maxx = Math.min(game.getMario().gridX() + 20, grid[0].length);
			for (int i = 0; i < entities.size(); i++) {
				Entity en = entities.get(i);
				if (en.gridX() < maxx && en.gridX() > minx) {
					en.tick();
				}else if (en.gridX() < minx) {
					entities.remove(en);
				}
			}
		}
		
		//things that shine, have different frames
		shineTick++;
		if (shineTick > 60) {
			shineTick = 0;
		}
		if (shineTick > 25 && shineTick % 8 == 0) {
			shineFrame++;
			if (shineFrame > 3) {
				shineFrame = 0;
			}
		}
	}

	//draws the map
	public void render(Graphics g) {
		int minx = Math.max(game.getMario().gridX() - 20, 0);
		int maxx = Math.min(game.getMario().gridX() + 20, grid[0].length);
		for (int i = 0; i < grid.length; i++) {
			for (int j = minx; j < maxx; j++) {
				if (grid[i][j] == 0) {
					continue;
				}else {
					int block = grid[i][j];
					if (block == COINBLOCK) block = 5;
					if (block > COINBLOCK) block = 3;
					if (block == 5) block += shineFrame;
					if (i == hit[1] && j == hit[0] && hitFrame < 10 && grid[hit[1]][hit[0]] != 4) {
						if (hitFrame < 5) {
							g.drawImage(tile[block], j * Game.BLOCK_SIZE - mapOffSetX, i * Game.BLOCK_SIZE - mapOffSetY - (hitFrame * 10), Game.BLOCK_SIZE, Game.BLOCK_SIZE, null);
						}else {
							g.drawImage(tile[block], j * Game.BLOCK_SIZE - mapOffSetX, i * 48 - mapOffSetY - (100 - hitFrame * 10), Game.BLOCK_SIZE, Game.BLOCK_SIZE, null);
						}
					}else {
						g.drawImage(tile[block], j * Game.BLOCK_SIZE - mapOffSetX, i * Game.BLOCK_SIZE + mapOffSetY, Game.BLOCK_SIZE, Game.BLOCK_SIZE, null);
					}
				}
			}
		}
		for (int i = 0; i < entities.size(); i++) {
			Entity en = entities.get(i);
			if (en.gridX() < maxx && en.gridX() > minx) {
				en.render(g);
			}
		}
	}
	
